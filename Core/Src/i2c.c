HAL_StatusTypeDef EEPROM_Write(uint16_t MemAddr, uint8_t Data) {
    // We use I2C_MEMADD_SIZE_16BIT because AT24C32 has > 256 bytes
    HAL_StatusTypeDef status = HAL_I2C_Mem_Write(&hi2c1, EEPROM_ADDR, MemAddr, 
                                                 I2C_MEMADD_SIZE_16BIT, &Data, 1, 100);
    // Crucial: The EEPROM needs ~5ms to complete the internal write cycle
    HAL_Delay(5); 
    return status;
}

HAL_StatusTypeDef EEPROM_PageWrite(uint16_t Addr, uint8_t *pData, uint16_t Size) {
    HAL_StatusTypeDef status;
    // 1. Send the data
    status = HAL_I2C_Mem_Write(&hi2c1, EEPROM_ADDR, Addr, 
                               I2C_MEMADD_SIZE_16BIT, pData, Size, 100);
    // 2. Wait for the EEPROM to finish writing (Twr = 5ms)
    HAL_Delay(5); 
    return status;
}

HAL_StatusTypeDef EEPROM_Read(uint16_t MemAddr, uint8_t *pData) {
    return HAL_I2C_Mem_Read(&hi2c1, EEPROM_ADDR, MemAddr, 
                            I2C_MEMADD_SIZE_16BIT, pData, 1, 100);
}

HAL_StatusTypeDef EEPROM_Read_Sequential(uint16_t Addr, uint8_t *pData, uint16_t Size) {
    return HAL_I2C_Mem_Read(&hi2c1, EEPROM_ADDR, Addr, 
                            I2C_MEMADD_SIZE_16BIT, pData, Size, 1000);
}

HAL_StatusTypeDef EEPROM_FullRead(uint8_t *pBuffer) {
    return EEPROM_Read_Sequential(0x0000, pBuffer, 4096);
}
